function Sum(A, B) {
    return A + B
}

function Sub(A, B) {
    return A - B
}

function Div(A, B) {
    return A / B
}

function Multi(A, B) {
    return A * B
}

function DoCalculations() {
    console.log(Sum(5, 1))
    console.log(Sub(5, 1))
    console.log(Div(5, 1))
    console.log(Multi(5, 1))
}

DoCalculations();